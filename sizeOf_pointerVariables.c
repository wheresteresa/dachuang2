// C program to print size of different types of pointer variables.
#include<stdio.h>

int main(){
  
  printf("\nSize of char pointer: %d",sizeof(char*));
  printf("\nSize of char pointer: %d",sizeof(int*));
  printf("\nSize of char pointer: %d",sizeof(float*));
  printf("\nSize of char pointer: %d",sizeof(long int*));
  printf("\nSize of char pointer: %d\n",sizeof(double*));
  return 0;
}
